    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        String input = txtFind.getText();
        
        
            if(input != null && input.length() > 0) {
                try{
                    int id = Integer.parseInt(input);
                    List<HoaDon> khList;
                    khList = HoaDonCtrl.findByIDkh_IDhd(MainFrame1.user, id);
                    tableModel.setRowCount(0);
                    khList.forEach(kh -> {
                        tableModel.addRow(new Object[] {tableModel.getRowCount() + 1, kh.getId(),kh.getNgay(),
                            kh.getId_kh(), kh.getTen_kh(), kh.getTongtien(), kh.getTrang_thai()});
                    });
                } catch(Exception e){
                    JOptionPane.showMessageDialog(this, "Mã hóa đơn phải là số...");
                }
                


            } else {
                //showStudent();
                JOptionPane.showMessageDialog(this, "Mã hóa đơn trống...");
            }
        
        
    }//GEN-LAST:event_btnFindActionPerformed


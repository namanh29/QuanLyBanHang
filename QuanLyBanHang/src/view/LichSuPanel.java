    public void showHoaDon() {
        if(rb1.isSelected()){
            hdList = HoaDonCtrl.findByIDkh_tt(MainFrame1.user, "Đã xác nhận");
        }
        else if(rb2.isSelected()){
            hdList = HoaDonCtrl.findByIDkh_tt(MainFrame1.user, "Chưa xác nhận");
        }
        else if(rb3.isSelected()){
            hdList = HoaDonCtrl.findByIDkh_tt(MainFrame1.user, "Bị hủy");
        }
        else if(rb.isSelected()){
            hdList = HoaDonCtrl.findByID_kh(MainFrame1.user);
        }
        else{
            hdList = HoaDonCtrl.findByID_kh(MainFrame1.user);
        }
        
        
        tableModel.setRowCount(0);
        
        hdList.forEach(kh -> {
            tableModel.addRow(new Object[] {tableModel.getRowCount() + 1, kh.getId(), kh.getId_kh(), kh.getNgay(),
                 kh.getTongtien(), kh.getTrang_thai()});
        });
        
    }

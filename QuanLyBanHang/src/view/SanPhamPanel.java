    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedIndex = tblKhachHang.getSelectedRow();
        if (selectedIndex >=0) {
            SanPham std = khList.get(selectedIndex);

            int option = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn muốn xóa sản phẩm này?");

            if (option ==0) {
                SanPhamCtrl.delete(std.getId());
                showSanPham();
            }
        }
        else {
            JOptionPane.showMessageDialog(this, "Bạn phải chọn một sản phẩm...");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


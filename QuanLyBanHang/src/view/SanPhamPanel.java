    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        String input = txtFind.getText();
        if(cbFind.getSelectedItem().toString().equals("Theo tên")){
            if(input != null && input.length() > 0) {
                khList = SanPhamCtrl.findByName(input);
                tableModel.setRowCount(0);
                khList.forEach(kh -> {
                    tableModel.addRow(new Object[] {tableModel.getRowCount() + 1, kh.getId(),kh.getTen(),
                        kh.getLoai(), kh.getHang(), kh.getMota(), kh.getGia(), kh.getBaohanh()});
                });
            }
            else {
                //tableModel = null;
                JOptionPane.showMessageDialog(this, "Tên sản phẩm trống...");
            }
        }
        if(cbFind.getSelectedItem().toString().equals("Theo loại sp")){
            if(input != null && input.length() > 0) {
                khList = SanPhamCtrl.findByType(input);
                tableModel.setRowCount(0);
                khList.forEach(kh -> {
                    tableModel.addRow(new Object[] {tableModel.getRowCount() + 1, kh.getId(),kh.getTen(),
                        kh.getLoai(), kh.getHang(), kh.getMota(), kh.getGia(), kh.getBaohanh()});
                });


            } else {
                //showStudent();
                JOptionPane.showMessageDialog(this, "Mã số khách hàng trống...");
            }
        }
        
    }//GEN-LAST:event_btnFindActionPerformed

